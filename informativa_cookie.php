<p class="p_cookie">Federica Placci utilizza cookie e tecnologie simili per garantire il corretto funzionamento delle procedure e migliorare l’esperienza di uso delle applicazioni online. Il presente documento fornisce informazioni dettagliate sull’uso dei cookie e di tecnologie similari, su come sono utilizzati da Chiarotondo e su come gestirli.
Definizioni</p>

<p class="p_cookie">I cookie sono brevi frammenti di testo (lettere e/o numeri) che permettono al server web di memorizzare sul client (il browser)
 informazioni da riutilizzare nel corso della medesima visita al sito (cookie di sessione) o in seguito, anche a distanza di
  giorni (cookie persistenti). I cookie vengono memorizzati, in base alle preferenze dell’utente, 
  dal singolo browser sullo specifico dispositivo utilizzato (computer, tablet, smartphone).</p>

<p class="p_cookie">Tecnologie similari, come, ad esempio, web beacon, GIF trasparenti e tutte le forme di storage locale introdotte con HTML5, 
sono utilizzabili per raccogliere informazioni sul comportamento dell’utente e sull’utilizzo dei servizi. </p>

<p class="p_cookie">Nel seguito di questo documento faremo riferimento ai cookie e a tutte le tecnologie similari utilizzando semplicemente il termine “cookie”.
Tipologie di cookie</p>

<p class="p_cookie">In base alle caratteristiche e all’utilizzo dei cookie possiamo distinguere diverse categorie:</p>

    <p class="p_cookie">Cookie strettamente necessari. Si tratta di cookie indispensabili per il corretto funzionamento di ogni sito e sono utilizzati 
    per gestire il login e l’accesso alle funzioni riservate del sito. La durata dei cookie è strettamente limitata alla sessione 
    di lavoro (chiuso il browser vengono cancellati). </br>
    Cookie di analisi e prestazioni. Sono cookie utilizzati per raccogliere e analizzare il traffico e l’utilizzo del sito in
     modo anonimo. Questi cookie, pur senza identificare l’utente, consentono, per esempio, di rilevare se il medesimo utente
      torna a collegarsi in momenti diversi. Permettono inoltre di monitorare il sistema e migliorarne le prestazioni 
      e l’usabilità. La disattivazione di tali cookie può essere eseguita senza alcuna perdita di funzionalità. </br>
    Cookie di profilazione. Si tratta di cookie permanenti utilizzati per identificare (in modo anonimo e non) le preferenze 
    dell’utente e migliorare la sua esperienza di navigazione. Il sito Chiarotondo Sistemi non utilizza cookie di questo tipo. </br>
	</p>

