<h3>Durata dei cookie</h3>

<p class="p_cookie">Alcuni cookie (cookie di sessione) restano attivi solo fino alla chiusura del browser o all’esecuzione del comando di logout. 
Altri cookie “sopravvivono” alla chiusura del browser e sono disponibili anche in successive visite dell’utente.</p>

<p class="p_cookie">Questi cookie sono detti persistenti e la loro durata è fissata dal server al momento della loro creazione. 
In alcuni casi è fissata una scadenza, in altri casi la durata è illimitata.</p>

<p class="p_cookie">Federica Placci non fa uso di cookie persistenti.</p>

<p class="p_cookie">Tuttavia, navigando sulle pagine del sito web di Federica Placci, si può interagire con siti gestiti da terze parti 
che possono creare o modificare cookie permanenti e di profilazione.</p>
Gestione dei cookie

<p class="p_cookie">L’utente può decidere se accettare o meno i cookie utilizzando le impostazioni del proprio browser.</p>

<p class="p_cookie">Attenzione: la disabilitazione totale o parziale dei cookie tecnici può compromettere l’utilizzo delle funzionalità del
 sito riservate agli utenti registrati. Al contrario, la fruibilità dei contenuti pubblici è possibile anche disabilitando 
 completamente i cookie.</p>

<p class="p_cookie">La disabilitazione dei cookie “terze parti” non pregiudica in alcun modo la navigabilità.</p>

<p class="p_cookie">L’impostazione può essere definita in modo specifico per i diversi siti e applicazioni web. Inoltre i migliori browser 
consentono di definire impostazioni diverse per i cookie “proprietari” e per quelli di “terze parti”.</p>

<p class="p_cookie">A titolo di esempio, in Firefox, attraverso il menu Strumenti->Opzioni ->Privacy, è possibile accedere ad un pannello di 
